package edu.uci.ics.fuzzyjoin.hadoop.ridrecordpairs;

import java.io.IOException;
import java.util.Iterator;

import org.apache.hadoop.io.NullWritable;
import org.apache.hadoop.io.Text;
import org.apache.hadoop.mapred.MapReduceBase;
import org.apache.hadoop.mapred.OutputCollector;
import org.apache.hadoop.mapred.Reducer;
import org.apache.hadoop.mapred.Reporter;

public class ReduceOne extends MapReduceBase implements
        Reducer<Object, Text, Text, NullWritable> {

    final private NullWritable nullWritable = NullWritable.get();

    public void reduce(Object unused, Iterator<Text> inputValues,
            OutputCollector<Text, NullWritable> collector, Reporter reporter)
            throws IOException {
        collector.collect(inputValues.next(), nullWritable);

    }
}
